package ru.spbpu.ellipsis.ellipsis;

/**
 * Типы глагольного эллипсиса по типологии Пархоменко В.А.
 */
public enum EllipsisType {

    /**
     * Тип 1: несколько подлежащих, сочинённые придаточные.
     * Структура: ИГ1_подл ГГр1_полная, [ИГi_подл ГГрi_неполная, i=2..n]
     * Пример: «диагональ AC перпендикулярна CD, а диагональ DB – AB»
     */
    TYPE_1_MULTIPLE_SUBJECTS,

    /**
     * Тип 2: одно подлежащее, сочинённые глагольные группы.
     * Структура: ИГ1_подл ГГр1_полная, [ГГрi_неполная, i=2..n]
     * Пример: «Окружность касается AB и AD, а сторону BC – в точке E»
     */
    TYPE_2_SINGLE_SUBJECT,

    /**
     * Тип 3: прочие — побудительные, неопределённо-личные, причастные обороты.
     * Пример: «Примите вершину C за начало координат, а ось проекций – за ось абсцисс»
     */
    TYPE_3_OTHER,

    UNKNOWN
}
